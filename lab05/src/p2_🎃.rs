struct Canvas([[u8; 100]; 55]);

impl Canvas{
    fn pixel(&mut self, x: u8, y: u8) -> &mut u8{
        &mut self.0[x as usize][y as usize]
    }
}

fn new_canvas() -> Canvas{
    Canvas([[32; 100]; 55])
}

fn set_pixels(canvas: &mut Canvas, pixel_array: &[(i32, i32, i32)]) -> (){
    for tuple in pixel_array{
        let x = tuple.0 as u8;
        let y = tuple.1 as u8;
        let character = tuple.2 as u8;
        let pixel = canvas.pixel(x, y);
        *pixel = character;
    }
}

fn print(canvas: Canvas) -> (){
    for line in canvas.0{
        for c in line{
            print!("{} ", c as char);
        }
        println!("");
    }
}

